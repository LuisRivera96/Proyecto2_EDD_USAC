    //ARCHIVOS
    private void JBArCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBArCrearActionPerformed
        // TODO add your handling code here:
        String nombreArchivo = JOptionPane.showInputDialog("Escribe el nombre del Archivo");
        String contenidoArchivo = JOptionPane.showInputDialog("Escribe el Contenido del Archivo");
        String timesTamp = pila.getTime();
        arboltemp.insertar(nombreArchivo, contenidoArchivo, timesTamp, usuarioActual);
        JButton archivo = new JButton(nombreArchivo);
        archivo.setIcon(new ImageIcon(getClass().getResource("/Iconos/archivo.png")));
        JPArchivos.add(archivo);
        archivos.add(archivo);
        indice++;
        JPArchivos.updateUI();
        pila.push("Creacion de Archivo",pila.getTime(),usuarioActual);
        pila.getGraphica();
        arboltemp.getGrafica();
    }//GEN-LAST:event_JBArCrearActionPerformed

