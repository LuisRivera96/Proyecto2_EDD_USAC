    private void JBCarCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBCarCrearActionPerformed
        // TODO add your handling code here:
        String nombreCarpeta = JOptionPane.showInputDialog("Escribe el nombre de la Carpeta");
        JButton carpeta = new JButton(nombreCarpeta);
        carpeta.setIcon(new ImageIcon(getClass().getResource("/Iconos/carpeta.jpeg")));
        JPArchivos.add(carpeta);
        carpetas.add(carpeta);
        indice++;
        JPArchivos.updateUI();
        pila.push("Creacion de Carpeta",pila.getTime(),usuarioActual);
        pila.getGraphica();
        
    }//GEN-LAST:event_JBCarCrearActionPerformed

